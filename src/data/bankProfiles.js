// Normalize bank name for lookup (handles various spellings)
export const normalizeBankName = (name) => {
    if (!name) return null;

    const mappings = {
        // National Bank
        "O'zbekiston Milliy banki": "National Bank",
        "Milliy Bank": "National Bank",
        "NBU": "National Bank",
        "Nat'l Bank UZ": "National Bank",

        // Xalq Bank
        "Xalq Banki": "Xalq Bank",
        "Xalq banki": "Xalq Bank",

        // SQB / Uzpromstroybank
        "O'zsanoatqurilishbank": "Uzpromstroybank",
        "Uzsanoat Bank": "Uzpromstroybank",
        "SQB": "Uzpromstroybank",

        // Asaka Bank
        "Asakabank": "Asaka Bank",

        // Ipoteka Bank
        "Ipoteka bank": "Ipoteka Bank",

        // Hamkorbank
        "Hamkor bank": "Hamkorbank",

        // Ipak Yuli
        "Ipak Yuli": "Ipak Yuli Bank",

        // Tenge Bank
        "Tenge bank": "Tenge Bank",

        // Infinbank
        "InfinBANK": "Infinbank",
        "Infinbank": "Infinbank",

        // BRB
        "BRB": "BRB Bank",

        // Aloqa Bank
        "Aloqabank": "Aloqa Bank",

        // Turon Bank
        "Turonbank": "Turon Bank",

        // Orient Finans
        "Orient Finans Bank": "Orient Finans Bank",
        "OFB": "Orient Finans Bank",

        // Trust Bank
        "Trastbank": "Trust Bank",
        "Trustbank": "Trust Bank",

        // Universal Bank
        "Universal bank": "Universal Bank",

        // Garant Bank
        "Garant bank": "Garant Bank"
    };

    // Check direct mapping
    if (mappings[name]) return mappings[name];

    // Check if already a valid key
    if (bankProfiles[name]) return name;

    // Try case-insensitive match
    const nameLower = name.toLowerCase();
    for (const key of Object.keys(bankProfiles)) {
        if (key.toLowerCase() === nameLower) return key;
    }

    // Try partial match
    for (const key of Object.keys(bankProfiles)) {
        if (nameLower.includes(key.toLowerCase()) || key.toLowerCase().includes(nameLower)) {
            return key;
        }
    }

    return null;
};

// Get bank profile by name (with normalization)
export const getBankProfile = (name) => {
    const normalized = normalizeBankName(name);
    return normalized ? bankProfiles[normalized] : null;
};
